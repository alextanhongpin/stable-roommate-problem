function shuffleArray(array) {
  for (let i = array.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [array[i], array[j]] = [array[j], array[i]];
  }
}

function generateList(n = 10) {
  const result = {};
  const arr = Array(n)
    .fill(0)
    .map((_, i) => i + 1);
  for (let i = 0; i < n; i++) {
    const tmp = new Set(arr);
    tmp.delete(i + 1);
    result[i + 1] = [...tmp];
    shuffleArray(result[i + 1]);
  }
  return result;
}

